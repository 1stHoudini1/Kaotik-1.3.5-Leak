package com.mawuote.client.gui.font;

private static class CharLocation
{
    private final int x;
    private final int y;
    private final int width;
    private final int height;
    
    CharLocation(final int x, final int y, final int width, final int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
